    nicht wieder nach Haus, und wir sind sie los." "Nein, Frau", sagte der Mann, "das 
    tue ich nicht; wie sollt' ich's übers Herz bringen, meine Kinder im Walde allein 
    zu lassen die wilden Tiere würden bald kommen und sie zerreißen." "O du Narr", 
